# EJERCICIO 1: Un estudiante está cursando 5 materias, tiene la nota de cada materia y quiere
# saber cuál es su promedio.

nota1 = int(input("Ingrese la primera nota: "))
nota2 = int(input("Ingrese la segunda nota: "))
nota3 = int(input("Ingrese la tercera nota: "))
nota4 = int(input("Ingrese la cuarta nota: "))
nota5 = int(input("Ingrese la quinta nota: "))

promedio = (nota1 + nota2 + nota3 + nota4 + nota5)/5

print('El promedio general de las cinco notas es: ', promedio)